package com.example.baytalmuqadas.models;

public class SuratModel {
    private String arti ;
    private String asma ;
    private String nama ;
    private String type ;
    private String audio ;
    private String jumlah_ayat ;
    private String nomor_surat ;
    private String keterangan ;

    public String getArti() {
        return arti;
    }

    public void setArti(String arti) {
        this.arti = arti;
    }

    public String getAsma() {
        return asma;
    }

    public void setAsma(String asma) {
        this.asma = asma;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getAudio() {
        return audio;
    }

    public void setAudio(String audio) {
        this.audio = audio;
    }

    public String getJumlah_ayat() {
        return jumlah_ayat;
    }

    public void setJumlah_ayat(String jumlah_ayat) {
        this.jumlah_ayat = jumlah_ayat;
    }

    public String getNomor_surat() {
        return nomor_surat;
    }

    public void setNomor_surat(String nomor_surat) {
        this.nomor_surat = nomor_surat;
    }

    public String getKeterangan() {
        return keterangan;
    }

    public void setKeterangan(String keterangan) {
        this.keterangan = keterangan;
    }


}
